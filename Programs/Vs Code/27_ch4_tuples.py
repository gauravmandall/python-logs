'''
Tuple methods
    Consider the following tuple
        a = (1, 7, 2)

    1> a.count(1) : a.count(1) will return number of times 1 occurs in a.
    2> a.index(1) : a.index(1) will return of first occurence of 1 in a.
'''


# Creating a tuple using ()
t = (1, 2, 4, 5, 4, 1, 2, 1, 1)

print(t.count(1))
print(t.index(1))