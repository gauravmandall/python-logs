'''
Escape Sequence Characters
    -Sequence of character after backslash'\'

    -Escape sequence character comprises of more than one 
     characters but represents one character when used 
     within the strings.

    -Examples --> \n(New line), \t(Tab), \'(Single quote), \\(backslash) etc.
'''

story = "Gaurav is good.\nHe\tis ve\\ry good"
print(story)