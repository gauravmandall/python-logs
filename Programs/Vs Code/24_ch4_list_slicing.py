'''
List Indexing\
    A list can be indexed just like a string
        L1 = [7, 9, "Gaurav"]
            L1 [0]  => 7
            L1 [1]  => 9
            L1 [70] => Error
            L1 [0:2] => [7,9] => List slicing
'''

# List Slicing
friends = ["Gaurav", "Raghav", "sam", "mark", "Shivam", 34]
print(friends[0:4])
print(friends[-4:])

